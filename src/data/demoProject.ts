import { field } from '../lib/fieldState'
import type { ApprovedItem, Project, RecommendationItem } from '../store/types'

function approved(
  base: Omit<RecommendationItem, 'status'>,
  quantity: number,
  notes = '',
): ApprovedItem {
  return { ...base, status: 'approved', quantity, notes }
}

function uid() {
  return ('DEMO' + Math.random().toString(36).slice(2, 7)).toUpperCase()
}

